 CREATE OR REPLACE VIEW FLIGHT_PATH AS
	SELECT idFlight,
		NAVPOINT.codeOACI,
		lon, lat,
		datetimePoint,
		distanceToPreviousNavPoint
	FROM AERODROME, NAVPOINT
	WHERE AERODROME.codeOACI = NAVPOINT.codeOACI 
	ORDER BY datetimePoint ASC;

