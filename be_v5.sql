--
--  Création de la vue FLIGHT_ENRICHED
-- 

-- Méthode 1 de calcul des Bonus: les noms d aéroport sont écrits en dur dans les requêtes SQL
--  pt positif : facile à faire (principe KISS respecté)
--  pt négatif : non évolutif

-- Méthode 2 : la table AERODROME_BONUS contient les aérodromes, pour faire ensuite une somme
--  pt positif, c'est évolutif
--  pt négatif, qd 2 aérodromes donnent le même code bonus, c'est plus complexe à calculer

-- On choisit donc la méthode 1 qui est plus simple, puisque l'évolution est improbable !


CREATE OR REPLACE VIEW FLIGHT_ENRICHED_HELPER AS
 	SELECT FLIGHT.idFlight, FLIGHT.nameTeam, FLIGHT.AircraftNumber, FLIGHT.loginUser,
 	 (SUM(inZone1) > 0) AS inZ1, (SUM(inZone2) > 0) AS inZ2, 
 	 (SUM(inZone3) > 0) AS inZ3, (SUM(inZone4) > 0) AS inZ4,
 	 (SUM(inZone5) > 0) AS inZ5, (SUM(inZone6) > 0) AS inZ6,
 	 (SUM(inCorse) > 0) AS inC, (SUM(isBonus1) > 0) AS inB1,
 	 (SUM(isBonus2) > 0) AS inB2, (SUM(isBonus3) > 0) AS inB3, 
 	 (SUM(isBonus4) > 0) AS inB4, (SUM(isBonus5) > 0) AS inB5,
	SUM( NP.distanceToPreviousNavPoint) AS totalDistance,
	COUNT( NP.codeOACI ) AS nbAerodromes,
	MIN( NP.datetimePoint ) AS beginFlight,
	MAX( NP.datetimePoint ) AS endFlight,
	( SELECT COUNT( codeOACI ) FROM NAVPOINT, FLIGHT WHERE FLIGHT.idFlight = NAVPOINT.idFlight AND NAVPOINT.codeOACI LIKE "LFK%" ) AS nbAerodromeCorse
 	FROM NAVPOINT AS NP, AERODROME_helper, FLIGHT
 	WHERE FLIGHT.idFlight = NP.idFlight AND NP.codeOACI = AERODROME_helper.codeOACI
 	GROUP BY FLIGHT.idFlight ;
 
CREATE OR REPLACE VIEW FLIGHT_ENRICHED_HELPER2 AS
 	SELECT idFlight, nameTeam, AircraftNumber, loginUser,
 	(inZ1+inZ2+inZ3+inZ4+inZ5+inZ6) AS nbConstraintSatisfied, 
 	(inB1+inB2+inB3+inB4+inB5) AS nbBonuses,
	IF( nbAerodromeCorse > 1, 1, 0 ) AS multiLandInCorse,
	inC,
 	totalDistance, nbAerodromes, beginFlight, endFlight
 	FROM FLIGHT_ENRICHED_HELPER ;

CREATE OR REPLACE VIEW FLIGHT_ENRICHED AS
 	SELECT idFlight, nameTeam, AircraftNumber, loginUser,
 	nbConstraintSatisfied, nbBonuses,
 	totalDistance, nbAerodromes, beginFlight, endFlight,
	IF( nbConstraintSatisfied < 6 , 0, 1) AS canBeRanked,
	IF( nbConstraintSatisfied < 6 , 
		0, -- Non classé car les contraintes ne sont pas respectées
		-- totalDistance - 80 * nbBonuses - 208 * inC - 80 * multiLandInCorse ) AS scoreSecondary -- distances en NM
		totalDistance - 148 * nbBonuses - 385 * inC - 148 * multiLandInCorse ) AS scoreSecondary -- distances en km
 	FROM FLIGHT_ENRICHED_HELPER2 ;
--	ORDER BY CanBeRanked DESC, nbAerodromes DESC, scoreSecondary DESC;

