DROP DATABASE IF EXISTS `beweb_2017`;
CREATE DATABASE IF NOT EXISTS `beweb_2017` DEFAULT CHARACTER SET latin1 COLLATE latin1_swedish_ci;
USE `beweb_2017`;

-- --------------------------------------------------------

--
-- Table structure for table `BEACON`
--

DROP TABLE IF EXISTS `BEACON`;
CREATE TABLE `BEACON` (
  `codeOACI` varchar(5) NOT NULL,
  `lon` double NOT NULL,
  `lat` double NOT NULL,
  `description` varchar(64) DEFAULT NULL,
  PRIMARY KEY (`codeOACI`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;


-- --------------------------------------------------------

--
-- Table structure for table `USER`
--

DROP TABLE IF EXISTS `USER`;
CREATE TABLE `USER` (
  `login` varchar(64) NOT NULL,
  `passwd` varchar(64) NOT NULL,
  `isAdmin` tinyint(1) NOT NULL,
  PRIMARY KEY (`login`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- --------------------------------------------------------

--
-- Table structure for table `FLIGHT`
--

DROP TABLE IF EXISTS `FLIGHT`;
CREATE TABLE `FLIGHT` (
  `idFlight` varchar(16) NOT NULL,
  `nameTeam` varchar(64) NOT NULL,
  `AircraftNumber` varchar(6) NOT NULL,
  `loginUser` varchar(64) NOT NULL,
  PRIMARY KEY (`idFlight`),
  FOREIGN KEY (`loginUser`) REFERENCES `USER` (`login`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- --------------------------------------------------------

--
-- Table structure for table `MAP_AREA`
--

DROP TABLE IF EXISTS `MAP_AREA`;
CREATE TABLE `MAP_AREA` (
  `no_zone` int(11) NOT NULL,
  PRIMARY KEY (`no_zone`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- --------------------------------------------------------

--
-- Table structure for table `METAR`
--

DROP TABLE IF EXISTS `METAR`;
CREATE TABLE `METAR` (
  `idMetar` int(11) NOT NULL AUTO_INCREMENT,
  `no_zone` int(11) NOT NULL,
  `windSpeed` double NOT NULL,
  `windDirection` int(11) NOT NULL,
  `datetimeMetar` datetime NOT NULL,
  PRIMARY KEY (`idMetar`),
  FOREIGN KEY (`no_zone`) REFERENCES `MAP_AREA` (`no_zone`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=latin1 AUTO_INCREMENT=1 ;

-- --------------------------------------------------------

--
-- Table structure for table `NAVPOINT`
--
-- datetimePoint == Landing

DROP TABLE IF EXISTS `NAVPOINT`;
CREATE TABLE `NAVPOINT` (
  `codeOACI` varchar(5) NOT NULL,
  `idFlight` varchar(16) NOT NULL,
  `datetimePoint` datetime NOT NULL, 
  `distanceToPreviousNavPoint` double DEFAULT 0,
  PRIMARY KEY (`codeOACI`,`idFlight`),
  KEY `idFlight` (`idFlight`),
  FOREIGN KEY (`codeOACI`) REFERENCES `BEACON` (`codeOACI`) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`idFlight`) REFERENCES `FLIGHT` (`idFlight`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=latin1;


